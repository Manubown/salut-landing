// One-off generator: lays out single-stroke cursive (Hershey 'scriptc') for the
// drawn intro words into src/app/pages/home/intro-letters.ts. Each glyph is a
// real centerline stroke, so stroke-dashoffset draws the pen ALONG the line
// (no outline border) and the letters connect. Runtime ships only the path
// strings — no font is loaded in the browser.
//
//   node scripts/gen-intro-paths.mjs
//
// Swap FONT (e.g. 'scripts', 'cursive', 'scriptc') / WORDS below and re-run.
import h from 'hersheytext';
import { writeFileSync } from 'node:fs';

const FONT = 'scriptc';
const PAD = 4; // breathing room around the glyphs
const TRACK = -1; // letter tracking in Hershey units (negative tightens/connects)

const WORDS = [
  ['hi', 'Hi'],
  ['hallo', 'Hallo'],
  ['ciao', 'Ciao'],
  ['salut', 'Salut'],
];

/** Shift every x coordinate in a Hershey "M x,y L x,y …" path by dx. */
const offsetX = (d, dx) =>
  d.replace(
    /(-?\d+(?:\.\d+)?),(-?\d+(?:\.\d+)?)/g,
    (_, x, y) => `${(+x + dx).toFixed(1)},${y}`,
  );

const entries = WORDS.map(([key, text]) => {
  const glyphs = h.renderTextArray(text, { font: FONT });
  const strokes = [];
  let originX = 0;
  for (const g of glyphs) {
    if (g.d) strokes.push(offsetX(g.d, originX));
    originX += (g.width || 18) + TRACK;
  }

  // tight bbox over every coordinate
  let minX = Infinity;
  let minY = Infinity;
  let maxX = -Infinity;
  let maxY = -Infinity;
  for (const s of strokes) {
    const nums = (s.match(/-?\d+(?:\.\d+)?/g) || []).map(Number);
    for (let i = 0; i + 1 < nums.length; i += 2) {
      const x = nums[i];
      const y = nums[i + 1];
      if (x < minX) minX = x;
      if (x > maxX) maxX = x;
      if (y < minY) minY = y;
      if (y > maxY) maxY = y;
    }
  }
  const x = minX - PAD;
  const y = minY - PAD;
  const w = maxX - minX + PAD * 2;
  const hgt = maxY - minY + PAD * 2;
  return {
    key,
    text,
    strokes,
    viewBox: `${x.toFixed(1)} ${y.toFixed(1)} ${w.toFixed(1)} ${hgt.toFixed(1)}`,
    ratio: +(w / hgt).toFixed(4),
  };
});

const ts = `// AUTO-GENERATED by scripts/gen-intro-paths.mjs — do not edit by hand.
// Single-stroke cursive (Hershey 'scriptc') for the drawn intro. Each entry's
// \`strokes\` are centerline glyph paths, drawn on with stroke-dashoffset (the
// pen follows the line). Regenerate: node scripts/gen-intro-paths.mjs
export interface IntroWord {
  /** stable id */
  key: string;
  /** the rendered word */
  text: string;
  /** one centerline path per glyph, positioned in viewBox space */
  strokes: string[];
  /** tight viewBox around the glyphs (with padding) */
  viewBox: string;
  /** width / height — used to size the inline SVG */
  ratio: number;
}

export const INTRO_WORDS: IntroWord[] = ${JSON.stringify(entries, null, 2)};
`;

writeFileSync('src/app/pages/home/intro-letters.ts', ts);
console.log(
  'wrote intro-letters.ts:',
  entries.map((e) => `${e.text} (${e.strokes.length} strokes, ratio ${e.ratio})`).join(', '),
);
